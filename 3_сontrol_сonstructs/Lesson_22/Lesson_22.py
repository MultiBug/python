# 22.1 Булева логика
print(1 == 1 and 2 == 2)
print(1 == 1 and 2 == 3)
print(1 != 1 and 2 == 2)
print(2 < 1 and 3 > 6)
####################################
####################################
# 22.2 Логический оператор or
print(1 == 1 or 2 == 2)
print(1 == 1 or 2 == 3)
print(1 != 1 or 2 == 2)
print(2 < 1 or 3 > 6)
####################################
####################################
# 22.3 Логический оператор not
print(not 1 == 1)
print(not 1 > 7)
